#9.	Crie um programa calcule a raiz quadrada e o cubo de um número inserido pelo usuário.
entrada=float(input("Entre com um numero para determinar sua raiz quadrada e seu cubo: "))
raizQuadrada=entrada**0.5
cubo=entrada**3
print("A raiz quadrada de {} é {} enquanto que seu cubo é {}".format(entrada,raizQuadrada,cubo))
